#include <unistd.h>
#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	size_t	sz;

	if (!s)
		return ;
	sz = ft_strlen(s);
	write(fd, s, sz);
	write(fd, "\n", 1);
}
