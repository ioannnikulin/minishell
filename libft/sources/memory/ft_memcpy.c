#include <stddef.h>

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	char		*d;
	const char	*s;

	if (n == 0)
		return (dest);
	d = dest;
	s = src;
	while (n --)
		d[n] = s[n];
	return (dest);
}
