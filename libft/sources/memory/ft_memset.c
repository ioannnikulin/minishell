#include <stddef.h>

void	*ft_memset(void *s, int c, size_t n)
{
	unsigned char	cc;
	unsigned char	*ret;

	if (n == 0)
		return (s);
	cc = (unsigned char)c;
	ret = (unsigned char *)s;
	while (n --)
		ret[n] = cc;
	return (s);
}
